/** Fast lookup by ISO alpha-2 code (built once at module load). */
const BY_CODE: ReadonlyMap<string, CountryOption> = new Map(
  COUNTRIES.map((country) => [country.code, country]),
);

/** Finds a country by ISO alpha-2 code (case-insensitive). Returns `undefined` if unknown. */
export function findCountry(code: string | undefined | null): CountryOption | undefined {
  if (!code) return undefined;
  return BY_CODE.get(code.toUpperCase());
}

/** Resolves the display name for a country code in the given app language ("en" | "es", defaults to English). */
export function countryName(code: string | undefined | null, lang: string | undefined | null): string {
  const country = findCountry(code);
  if (!country) return '';
  return lang === 'es' ? country.es : country.en;
}
